export function buildSystemPrompt(style: RoastStyle): string {
  const stylePrompt = STYLE_PROMPTS[style] || STYLE_PROMPTS.savage;
  return `${BASE_SYSTEM_PROMPT}\n\n${stylePrompt}`;
}

export function buildUserPrompt(resumeText: string): string {
  return `请严格按照上述评分体系和风格要求，分析以下简历内容，给出 8 条吐槽和具体改进建议。

注意：
- 每条吐槽必须引用简历中的具体原文
- 每条建议必须包含改进前后对比
- 评分必须基于四个维度的客观评估
- 禁止使用"建议优化""有待提升"等空话

简历内容：
---
${resumeText}
---`;
}

